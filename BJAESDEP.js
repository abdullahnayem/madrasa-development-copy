    function buildDeck() {
      navList.innerHTML = slides
        .map(
          (s, i) =>
            `<li><button data-index="${i}" class="${i === 0 ? "active" : ""}">${i + 1}. ${s.title}</button></li>`
        )
        .join("");

      deck.innerHTML = slides
        .map(
          (s, i) => `
            <article class="slide ${i === 0 ? "active" : ""}" data-slide="${i}">
              <header class="slide-head">
                <h2>${s.title}</h2>
                <p>${s.subtitle}</p>
              </header>
              <div class="slide-content">${s.render()}</div>
            </article>
          `
        )
        .join("");

      navList.addEventListener("click", (e) => {
        const btn = e.target.closest("button[data-index]");
        if (!btn) return;
        goToSlide(Number(btn.dataset.index));
      });

      prevBtn.addEventListener("click", () => goToSlide(currentSlide - 1));
      nextBtn.addEventListener("click", () => goToSlide(currentSlide + 1));

      document.addEventListener("keydown", (e) => {
        if (e.key === "ArrowRight" || e.key === "PageDown") goToSlide(currentSlide + 1);
        if (e.key === "ArrowLeft" || e.key === "PageUp") goToSlide(currentSlide - 1);
      });

      updateUi();
      runSlideHook();
    }

    function goToSlide(index) {
      if (index < 0 || index >= slides.length) return;
      currentSlide = index;
      const allSlides = [...deck.querySelectorAll(".slide")];
      const allButtons = [...navList.querySelectorAll("button")];

      allSlides.forEach((s, i) => s.classList.toggle("active", i === currentSlide));
      allButtons.forEach((b, i) => b.classList.toggle("active", i === currentSlide));

      updateUi();
      runSlideHook();
    }

    function updateUi() {
      slideCounter.textContent = `${currentSlide + 1} / ${slides.length}`;
      progressBar.style.width = `${((currentSlide + 1) / slides.length) * 100}%`;
      prevBtn.disabled = currentSlide === 0;
      nextBtn.disabled = currentSlide === slides.length - 1;
    }

